<template>
  <div>
    <el-tabs v-model="active" v-if="name && formConfig.length > 0">
      <el-tab-pane :label="name" name="properties">
        <component :is="(selectNode.type||'').toLowerCase()" :config="selectNode.props"/>
      </el-tab-pane>
      <!-- <el-tab-pane label="表单权限设置" name="permissions">
        <form-authority-config/>
      </el-tab-pane> -->
    </el-tabs>
    <component :is="(selectNode.type||'').toLowerCase()" v-else :config="selectNode.props"/>
  </div>
</template>

<script lang="ts">
  import {
    ref,
    toRefs,
    reactive,
    computed,
    onMounted,
    defineComponent,
    getCurrentInstance,
    ComponentInternalInstance
  } from 'vue';
  import Approval from './ApprovalNodeConfig.vue'
  import Concurrent from './ConditionNodeConfig.vue'
  import Condition from './ConditionNodeConfig.vue'
  import Delay from './DelayNodeConfig.vue'
  import Cc from './CcNodeConfig.vue'
  import Trigger from './TriggerNodeConfig.vue'
  import FormAuthorityConfig from './FormAuthorityConfig.vue'
  import Root from './RootNodeConfig.vue'
  
  export default defineComponent({
    name: 'NodeConfig',
    components: {
      Approval,
      Concurrent,
      Condition,
      Trigger,
      Delay,
      Root,
      Cc,
      FormAuthorityConfig
    },
    setup(props, { emit }) {
      const { appContext  } = getCurrentInstance() as ComponentInternalInstance;
      const proxy = appContext.config.globalProperties;
      //
      const state = reactive({ active: 'properties',});
      //
      const selectNode:any = computed(() => {
        return proxy.$pinia.state.value.appwfConfig.selectedNode
      });
      const formConfig = computed(() => {
        return proxy.$pinia.state.value.appwfConfig.formItems
      });
      const name = computed(() => {
        switch (selectNode.type) {
          case 'ROOT':
            return '设置发起人';
          case 'APPROVAL':
            return '设置审批对象';
          case 'CC':
            return '设置抄送对象';
          default:
            return null;
        }
      });
      // 页面加载时
      onMounted(() => {
      });
      return {
        proxy,
        ...toRefs(state),
        //
        selectNode,
        formConfig,
        name,
      };
    },
  });
</script>

